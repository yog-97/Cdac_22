
class ass3_pyramid_11
{
	public static void main(String args[])
	{
		for(int i=1;i<=5;i++)
		{
			for( int l = 2; l<=i ; l++)
			{
				
				System.out.print(" ");
			}
			
			for( int j = 1; j<=5 ; j++)
			{
				
				System.out.print("*");
			}
			
			
			System.out.println();
			
		}
	}	
	
	
}	