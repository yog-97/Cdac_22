
class ass3_pyramid_10
{
	public static void main(String args[])
	{
		for(int i=5;i>=1;i--)
		{
			for( int j = 2; j<=i ; j++)
			{
				
				System.out.print(" ");
			}
			
			for( int l = 5; l>=1 ; l--)
			{
				
				System.out.print("*");
			}
			System.out.println();
			
			
		}
	}	
	
	
}	